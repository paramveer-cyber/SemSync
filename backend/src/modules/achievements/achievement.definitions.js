const buildTriggerIndex = (definitions) => {
    const index = new Map();
    for (const definition of definitions) {
        for (const trigger of definition.triggers) {
            const existing = index.get(trigger) ?? [];
            existing.push(definition);
            index.set(trigger, existing);
        }
    }
    return index;
};

const triggerIndex = buildTriggerIndex(ACHIEVEMENT_DEFINITIONS);

export const getDefinitionsForTrigger = (trigger) =>
    triggerIndex.get(trigger) ?? [];

