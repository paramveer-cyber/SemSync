function hexToRgb(hex: string): [number, number, number] {
    const parsedHex = parseInt(hex.replace('#', ''), 16);
    return [(parsedHex >> 16) & 255, (parsedHex >> 8) & 255, parsedHex & 255];
}

function darken(hex: string, amount: number): string {
    const [red, green, blue] = hexToRgb(hex);
    const darkenChannel = (channel: number) =>
        Math.round(channel * (1 - amount));
    return `#${[darkenChannel(red), darkenChannel(green), darkenChannel(blue)]
        .map((channel) => channel.toString(16).padStart(2, '0'))
        .join('')}`;
}

function lighten(hex: string, amount: number): string {
    const [red, green, blue] = hexToRgb(hex);
    const lightenChannel = (channel: number) =>
        Math.round(channel + (255 - channel) * amount);
    return `#${[
        lightenChannel(red),
        lightenChannel(green),
        lightenChannel(blue),
    ]
        .map((channel) => channel.toString(16).padStart(2, '0'))
        .join('')}`;
}

export function buildCustomThemeVars(
    brandHex: string,
    dark: boolean,
): Record<string, string> {
    const [red, green, blue] = hexToRgb(brandHex);

    if (dark) {
        const dimHex = darken(brandHex, 0.25);
        const activeTextHex = lighten(brandHex, 0.2);
        return {
            '--color-brand': brandHex,
            '--color-brand-dim': dimHex,
            '--color-brand-glow': `rgba(${red},${green},${blue},0.22)`,
            '--color-surface': '#050505',
            '--color-surface-1': '#0e0e0e',
            '--color-surface-2': '#161616',
            '--color-surface-3': '#1e1e1e',
            '--color-text': '#f7f7f7',
            '--color-text-muted': 'rgba(255,255,255,0.65)',
            '--color-text-faint': 'rgba(255,255,255,0.38)',
            '--color-glass': 'rgba(255,255,255,0.03)',
            '--color-glass-border': 'rgba(255,255,255,0.12)',
            '--color-glass-hover': 'rgba(255,255,255,0.06)',
            '--color-sidebar-bg': 'rgba(4,4,4,0.98)',
            '--color-sidebar-border': 'rgba(255,255,255,0.08)',
            '--color-active-bg': `rgba(${red},${green},${blue},0.14)`,
            '--color-active-text': activeTextHex,
            '--color-outline-variant': 'rgba(255,255,255,0.14)',
            '--color-secondary': brandHex,
        };
    } else {
        const dimHex = darken(brandHex, 0.15);
        return {
            '--color-brand': brandHex,
            '--color-brand-dim': dimHex,
            '--color-brand-glow': `rgba(${red},${green},${blue},0.15)`,
            '--color-surface': '#dde1e7',
            '--color-surface-1': '#e8ecf0',
            '--color-surface-2': '#d4d8de',
            '--color-surface-3': '#c9cdd3',
            '--color-text': '#0a0a0a',
            '--color-text-muted': 'rgba(10,10,10,0.62)',
            '--color-text-faint': 'rgba(10,10,10,0.38)',
            '--color-glass': 'rgba(0,0,0,0.04)',
            '--color-glass-border': 'rgba(0,0,0,0.12)',
            '--color-glass-hover': 'rgba(0,0,0,0.07)',
            '--color-sidebar-bg': 'rgba(220,224,230,0.97)',
            '--color-sidebar-border': 'rgba(0,0,0,0.12)',
            '--color-active-bg': `rgba(${red},${green},${blue},0.12)`,
            '--color-active-text': brandHex,
            '--color-outline-variant': 'rgba(0,0,0,0.12)',
            '--color-secondary': brandHex,
        };
    }
}
